export const App: FC<{ name: string }> = ({ name }) => {
  const [visiblePosts, setVisiblePosts] = useState(10); // Number of posts to initially display
  const [loading, setLoading] = useState(false);
  const [data, setData] = useState(
    socialMediaPostsDummy
      .map((item) => {
        return { isChecked: false, ...item };
      })
      .slice(0, visiblePosts)
  );
  useEffect(() => {
    // Simulate fetching data from an API
    setLoading(true);
    setTimeout(() => {
      const nextPosts = socialMediaPostsDummy
        .map((item, index) => {
          const existingItem = data.find((item, indexx) => indexx === index);
          return {
            isChecked: existingItem ? existingItem.isChecked : false,
            ...item,
          };
        })
        .slice(0, visiblePosts + 5);

      setData(nextPosts);
      setLoading(false);
    }, 1000); // Simulated loading delay
  }, [visiblePosts]);

  const loadMore = () => {
    if (!loading) {
      setVisiblePosts(visiblePosts + 5);
    }
  };

  window.onscroll = () => {
    if (
      window.innerHeight + document.documentElement.scrollTop ===
      document.documentElement.offsetHeight
    ) {
      loadMore();
    }
  };
  return (
    <div>
      {JSON.stringify(data.filter((item) => item.isChecked === true).length)}
      <div>
        <input
          type="checkbox"
          onChange={(e) => {
            setData(
              data.map((item) => {
                const { isChecked, ...props } = item;
                return { isChecked: e.target.checked, ...props };
              })
            );
          }}
        />
        <label htmlFor="">Select All</label>
      </div>
      {data.map((item, index) => {
        return (
          <div key={index}>
            <p>{item.category}</p>
            <input
              type="checkbox"
              onChange={(e) => {
                data.find((_, indexx) => index === indexx).isChecked =
                  e.target.checked;
                setData([...data]);
              }}
              checked={item.isChecked}
            />
          </div>
        );
      })}
      {loading && <p>Loading...</p>}
    </div>
  );
};
